               <h6 class="mb-0">
                 Projeto NJOY obtém o 3º lugar no Prémio Fundação Ilídio Pinho – Ciência na Escola.
               </h6>
                <p>Foram anunciados dia 9 de janeiro, durante a mostra nacional dos 100 melhores projetos, 
                    os resultados do Prémio Fundação Ilídio Pinho – Ciência na Escola, que este ano teve 
                    como tema “A ciência na escola ao serviço do desenvolvimento e da humanização”.</p>
                <p>Concorreram 1269 projetos, tendo o NJOY conquistado o 3º lugar no escalão destinado 
                    a projetos com o envolvimento de alunos do secundário.</p>
                <p>O NJOY pretende fazer uso das novas tecnologias para auxiliar pessoas com distrofia 
                    muscular e com algum tipo de necessidades especiais ao nível da comunicação e da aprendizagem. 
                    Surgiu como uma ideia dos alunos de uma turma do curso profissional de Técnico de Gestão 
                    de Equipamentos Informáticos da Escola Secundária Mouzinho da Silveira (ESMS), precisamente 
                    para ajudar um aluno da ESMS que sofre de distrofia muscular. Foi um projeto que o Politécnico 
                    acolheu com grande entusiasmo e carinho e que envolveu professores e alunos dos cursos 
                    profissionais da área da Informática e do Design da ESMS, bem como do curso de Engenharia 
                    Informática da ESTG/IPP, num bom exemplo de multidisciplinaridade e de articulação de vários
                    níveis de ensino.</p>
