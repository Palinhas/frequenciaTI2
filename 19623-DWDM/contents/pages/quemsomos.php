<h4 align="justify"><strong>Escola Superior de técnologia e gestão</strong></h4>
<p align="justify">A Escola Superior Tecnologia e Gestão do Instituto Politécnico de Portalegre (IPP), 
é um estabelecimento público de ensino superior politécnico, sendo uma unidade orgânica 
de ensino e investigação integrada no IPP, que tem como missão criar, transmitir e difundir 
o conhecimento, orientado para os domínios das ciências económicas e das organizações, 
tecnologias e engenharia, e design, artes e animação através da formação e qualificação 
de alto nível para públicos diferenciados, em momentos vários dos percursos académico 
e profissional e da investigação e desenvolvimento tecnológico para a promoção das comunidades, 
em cooperação com entidades regionais, nacionais e internacionais, ​distinguindo-se pela oferta 
inovadora e pela personalização ​do ensino.​
A escola possui acesso a meios informáticos e a redes de comunicação para toda a comunidade escolar, 
salas de aula com meios audiovisuais disponíveis e computadores, espaço de Intranet (permite 
o acesso à distância a cadeiras online), biblioteca (livre acesso, com acesso à B-on, e computadores 
com acesso à rede), salas de trabalho, auditório e anfiteatros.<p>
