                    <div><h3><strong>GESTÃO DE PME</strong></h3>

                        <p><strong>Objetivos gerais</strong></p>

                        <p>Este curso de mestrado visa proporcionar uma formação distintiva, que privilegie o estudo das mais recentes tendências das ciências empresariais, sempre numa perspetiva de aplicação prática dos conceitos ao tecido empresarial, particularmente às pequenas e médias empresas.</p>

                        <p>Paralelamente, pretende-se que o Mestrado em Gestão de PME possa apoiar o desenvolvimento da região de influência do Instituto Politécnico de Portalegre, através de uma forte ligação às empresas e instituições regionais e pelo contributo para o surgimento de novos projetos empresariais e para a fixação de pessoas.</p>

                        <p><strong>Objetivos de aprendizagem</strong></p>

                        <p>Pretende-se que os estudantes possam adquirir conhecimentos e competências que lhes permitam intervir diretamente na gestão de empresas, independentemente do perfil que possam assumir (empresário, diretor técnico, coordenador de projetos…)</p>

                        <p><strong>Saídas profissionais</strong></p>

                        <p>O curso tem como objetivo formar profissionais que fiquem aptos a:</p>

                        <ul>
                            <li>Transformar uma ideia em negócio e a conceber o respetivo plano;</li>
                            <li>Compreender o comportamento humano nas organizações, desenvolvendo a capacidade de liderança e de negociação;</li>
                            <li>Proceder à estruturação e reestruturação financeira de empresas;</li>
                            <li>Entender o sistema logístico empresarial como fonte potencial de vantagem competitiva;</li>
                            <li>Tomar decisões estratégicas e operacionais de marketing e aplicar conhecimentos, instrumentos e metodologias no planeamento de marketing;</li>
                            <li>Conceber, implementar e monitorizar a execução de planos estratégicos empresariais;</li>
                            <li>Conceber e implementar sistemas de inovação;</li>
                            <li>Utilizar instrumentos de gestão da performance organizacional e aplicá-los em contexto de trabalho.</li>
                        </ul>

