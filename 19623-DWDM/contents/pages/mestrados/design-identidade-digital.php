                    <h3><strong>Design de Identidade Digital</strong></h3>

                    <p style="margin-left: 40px;"><strong>Objetivos gerais</strong></p>

                    <p style="margin-left: 40px;" dir="ltr">Este Mestrado tem como objectivo capacitar os alunos para o desenvolvimento de projetos no universo da criação da imagem e comunicação da identidade das empresas, das organizações e dos produtos. As estratégias das marcas, os conceitos e os meios envolvidos nas relações entre os diferentes intervenientes atravessam um período de transformação acelerada, em parte, forçada pela evolução e ubiquidade dos media digitais.</p>

                    <p style="margin-left: 40px;" dir="ltr">É nesse sentido que se pretendem desenvolver competências aplicáveis aos atuais projetos de identidade e comunicação das marcas e proporcionar uma formação especializada para fazer face às exigências do mercado e da sociedade, seguindo uma perspectiva renovadora, cruzando conhecimentos do design, da animação multimédia, gestão das marcas e das comunicações nas redes digitais, interações entre os indivíduos, as organizações e os produtos, com especial enfoque no tecido empresarial local, rico em produtos endógenos e relações de proximidade.</p>

                    <p style="margin-left: 40px;"><strong>Objetivos de aprendizagem</strong></p>

                    <p style="margin-left: 40px;">O curso conduz a um entendimento actual da construção da imagem e da formação da identidade de uma marca e das suas interligações com factos da cultura, tendências sociais e comportamentais do presente, habilitando para a intervenção prática ao nível do design da identidade visual, das animações gráficas destinadas aos media digitais e da gestão da imagem das marcas, capacitando para projetos de branding e design de identidade visual: identificação, gestão e comunicação das qualidades e dos valores de uma marca; trabalho criativo de definição de cenários inovadores adequados à comunicação das marcas; representação de ideias e conceitos inovadores; gestão da presença nos media a diferentes escalas e meios, dos tradicionais aos digitais; produção de objetos associados à comunicação da identidade, visando a exploração visual dinâmica dos elementos gráficos; aprofundamento de componentes essenciais à linguagem visual, em conjunto com elementos fundamentais em projetos de animação multimedia.</p>

                    <p style="margin-left: 40px;"><strong>Saidas Profissionais</strong></p>

                    <ul style="margin-left: 40px;" dir="ltr">
                        <li>Integrar equipas de criativos em gabinetes de design e agências de comunicação e publicidade&nbsp;dedicadas ao&nbsp;branding;</li>
                        <li>Integrar gabinetes de comunicação em empresas e instituições focados na criação e gestão de uma identidade dinâmica com aplicações&nbsp;multimédia,&nbsp;online&nbsp;&lt;wbr/&gt;e&nbsp;offline;</li>
                        <li>Desenvolver projetos de identidade e comunicação digital – incluindo os aspectos estratégicos, a gestão, a concepção de soluções e a respectiva produção.</li>
                        <li>Estabelecer gabinetes autónomos de design de comunicação e animação multimédia, com competências para desenvolver projetos de identidade.&nbsp;</li>
                    </ul>

