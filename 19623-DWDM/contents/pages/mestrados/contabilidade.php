                    <div><h3><strong>CONTABILIDADE E FINANÇAS</strong></h3>

                        <p><strong>Objetivos</strong></p>

                        <p>O curso visa formar profissionais com elevada competência técnica que possam ir ao encontro das necessidades de informação das organizações, quer a nível interno, quer a nível externo, na área da Contabilidade e Finanças. Assim, são objetivos do curso:</p>

                        <ul>
                            <li>Proporcionar aos alunos uma sólida formação com vista a um melhor desempenho no exercício das suas funções e à valorização progressiva das suas carreiras;</li>
                            <li>Aprofundar conhecimentos e criar mecanismos de consulta e pesquisa que permitam, de uma forma rigorosa e sólida, enfrentar os problemas subjacentes à Contabilidade e Finanças;</li>
                            <li>Dar a conhecer os métodos e técnicas que permitam uma intervenção eficaz na área financeira e que contribuam para a criação de valor nas organizações;</li>
                            <li>Apresentar as mais recentes tendências de desenvolvimento da Contabilidade e Finanças.</li>
                            <li>O curso visa ainda contribuir para o desenvolvimento da região (de interior) em que se insere, através do aumento das capacidades de gestão financeira das empresas.</li>
                        </ul>

