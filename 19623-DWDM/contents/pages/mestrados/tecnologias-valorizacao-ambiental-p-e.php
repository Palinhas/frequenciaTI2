                    <div><h3><strong>Tecnologias de Valorização Ambiental e Produção de Energia</strong></h3>

                        <h3><strong><a href="http://jmaia.pt/site.tvapee/">Environmental Protection Technologies and&nbsp;Energy Production</a></strong></h3>

                        <p><strong>Objetivos</strong></p>

                        <p>O Mestrado em Tecnologias de Valorização Ambiental e Produção de Energia tem como objetivo conferir a licenciados, em diferentes áreas da engenharia, competências nos campos da valorização e tratamento de resíduos e da produção de energias a partir de fontes renováveis e que sejam alternativas às fontes mais tradicionais, nomeadamente, os combustíveis fósseis. Pretende-se apresentar uma visão integrada e multidisciplinar dos dois assuntos no sentido de se poder dar respostas tecnológicas e científicas consistentes e evoluídas à necessidade crescente da sociedade moderna, das suas instituições e industrias, em desenvolver e aplicar tecnologias limpas para tratar os seus resíduos e de produzir energia a partir de fontes alternativas aos combustíveis fósseis.</p>

                        <p><strong>Competências a desenvolver</strong></p>

                        <p>O Mestrado em&nbsp;Tecnologias de Valorização Ambiental e Produção de Energia tem como objectivo conferir competências aos formandos de conceptualização, aplicação, desenho, projecto e implementação de sistemas de produção de energias renováveis e de tratamento de resíduos, nomeadamente:</p>

                        <ul>
                            <li>tratamento de resíduos sólidos urbanos (RSU) e biomássicos;</li>
                            <li>monitorização ambiental;</li>
                            <li>células de combustível e tecnologia do hidrogénio;</li>
                            <li>sistemas fotovoltáicos;</li>
                            <li>sistemas eólicos;</li>
                            <li>sistemashídricos;</li>
                            <li>sistemas de racionalização energética, tais como a co-geração e melhoria da eficiência energética em edifícios de escritórios, fábricas, entre outros.</li>
                        </ul>

