                    <h3>Tecnologias&nbsp;de Produção de Biocombustíveis</h3>

                    <p><strong>Objetivos</strong></p>

                    <p align="justify">A licenciatura em Tecnologias&nbsp;de Produção de Biocombustíveis, compreende os processos tecnológicos de produção de combustíveis a partir de resíduos agro-indústrias ou de culturas energéticas, assim como toda a cadeia de produção de biocombústiveis, desde a produção agrícola até ao aproveitamento e minimização dos impactos dessa produção, incluindo as implicações ambientais da utilização dos próprios biocombustíveis.</p>

                    <hr>
                    <p><strong>Saídas profissionais</strong></p>

                    <ul>
                        <li>Gestores de produção de biocombustíveis ou outros produtos de biorrefinação ou de sistemas de trtamaentos de resíduos em empresas agrícolas e florestais dedicadas ou de fileira;</li>
                        <li>Gestores ou assistentes de gestão de sistemas de aproveitamento de resíduos em unidades da indústria agro-alimentar e transformadora;</li>
                        <li>Auxiliares de investigação em instituições de ensino, científicas ou de desenvolvimento tecnológico;</li>
                        <li>Técnicos superiores em gabinetes de projeto e consultoria.&nbsp;</li>
                    </ul>

