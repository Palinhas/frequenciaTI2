                    <div><h3>Design e Animação Multimédia</h3></div>

                    <p><strong>Objetivos&nbsp;</strong></p>

                    <p>Face à constante evolução tecnológica, constata-se cada vez mais uma fusão entre diferentes áreas de conhecimento. Assim, o curso proposto pretende proporcionar&nbsp;uma formação base que associe competências de Animação, com o Design de Comunicação, aliando à capacidade científica e conceptual, uma forte componente&nbsp;prática. Tendo a sua estrutura curricular como principal objetivo formar profissionais na Área da Animação, com competências para desenvolver projetos e integrar&nbsp;equipas multidisciplinares. Para além de competências específicas enquanto animador (keyframing, inbetweening e lip-sync) e desenhador, o profissional deverá estar&nbsp;dotado de conhecimentos nas técnicas de coloração, cleanup, programação, captação de imagens e montagem de vídeo. No âmbito da animação tridimensional, o&nbsp;animador formado por esta licenciatura saberá aplicar ao 3D as regras e princípios básicos da animação. Será ainda competente nas áreas da modelação de cenários e&nbsp;personagens na texturização e iluminação.</p>

                    <hr>
                    <p><strong>Saídas profissionais</strong></p>

                    <ul>
                        <li>Produtos de filmes de animação;</li>
                        <li>Desenhador de figuras cenários e ambientes, permitindo criar personagens e fundos para desenhos animados e produção de storyboards;</li>
                        <li>Animação no domínio dos jogos de computador;</li>
                        <li>Produtoras de imagem e vídeo;</li>
                        <li>Agências de publicidade;</li>
                        <li>Produção gráfica;</li>
                    </ul>

