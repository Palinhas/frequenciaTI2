                            <h3>Engenharia Informática</h3>

                            <p><strong>Objetivos</strong></p>

                            <p align="justify">O&nbsp;curso tem por objectivo formar profissionais capazes de exercer a sua actividade profissional com um elevado nível de competência técnica e científica.&nbsp;Tem uma formação de base sólida, que abrange um espectro alargado, independentemente das aplicações, nos domínios da Arquitectura e Desenvolvimento de Sistemas de Informação, Desenvolvimento Aplicacional, Infra-estruturas, Multimédia, Software de Entretenimento, Realidade Virtual, Aspectos Organizacionais, de Gestão e Sócio Económicos dos Sistemas de Informação – e nas áreas cientificas, tecnológicas e socio-económicas que lhe estão subjacentes.&nbsp;A grande maioria das unidades curriculares do Curso tem uma forte componente prática, com trabalhos e projectos envolvendo implementação concreta de aplicações e sistemas. Nas unidades curriculares mais avançadas esta componente é virada para as necessidades do mercado.</p>

                            <ul>
                            </ul>

                            <hr>
                            <p><strong>Saídas profissionais</strong></p>

                            <ul>
                                <li>Implementação e gestão de sistemas informáticos</li>
                                <li>Concepção e planeamento de sistemas de informação</li>
                                <li>Gestão e administração de sistemas de gestão de bases de dados</li>
                                <li>Concepção, desenvolvimento e manutenção de redes de computadores</li>
                                <li>Análise e programação</li>
                                <li>Gestão de equipas multidisciplinares</li>
                                <li>Concepção e desenvolvimento de projectos e produtos multimédia, por exemplo na área do entretenimento e jogos</li>
                                <li>Produção de Vídeo Jogos em Multi-plataformas</li>
                                <li>Produções e Animações para televisão, Vídeo, Cinema e Internet</li>
                            </ul>

