                    <div><h3>Administração de Publicidade e Marketing</h3>

                        <p><strong>Objetivos</strong></p>

                        <p align="justify">A Licenciatura em Administração de Publicidade e Marketing assegura o desenvolvimento das competências dos seus diplomados no planeamento, execução e controle das atividades de administração de empresas e organizações. O plano de estudos é composto por disciplinas que permitem aos alunos obter uma perspetiva da sua área de estudos. Garantindo a compreensão global do funcionamento de uma empresa, do papel desempenhado pelo marketing no seu funcionamento e na aquisição das competências necessárias para dar resposta à evolução dos mercados. Durante o 6º semestre, realiza-se um Estágio, com o objetivo do aluno utilizar as competências adquiridas num contexto empresarial. Cada aluno é acompanhado por um orientador interno, professor da Escola, e por um orientador externo, responsável da empresa de acolhimento. Existe a possibilidade de efetuar parte desse percurso noutras instituições de ensino superior europeias, ao abrigo de programas de cooperação existentes.</p>

