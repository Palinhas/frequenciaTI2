                    <div><h3>DESIGN DE COMUNICAÇÃO</h3></div>

                    <p><strong>Objetivos</strong></p>

                    <p align="justify">De acordo com o enquadramento legal no ensino politécnico, a licenciatura em Design de Comunicação valoriza a formação que visa o exercício da actividade profissional, tendo por objectivos a preparação dos alunos para a correcta resolução técnica, estética, funcional e metodológica de projectos de Design.</p>

                    <p align="justify">Durante o percurso de ensino/aprendizagem promovem-se as capacidades de resposta dos alunos ao nível criativo e de expressão pessoal, em conjugação com as capacidades de manipulação das tecnologias actualmente disponíveis para a edição e produção de conteúdos de comunicação visual. Em simultâneo, potencia-se a autonomia do aluno bem como a consciência para a importância de uma colaboração interdisciplinar e multidisciplinar, fundamentais ao desenvolvimento do trabalho científico e profissional na área.</p>

                    <ul>
                    </ul>

                    <hr>
                    <p><strong>Saídas profissionais</strong></p>

                    <ul>
                        <li>Assegurar ou assessorar a direcção de gabinetes de comunicação interna ou externa em empresas e instituições;</li>
                        <li>Integrar quadros criativos de agências de publicidade e de gabinetes de design;</li>
                        <li>Criar e gerir gabinetes autónomos de comunicação, design, animação;</li>
                        <li>Utilizar e gerir meios de Desktop publishing.</li>
                    </ul>

