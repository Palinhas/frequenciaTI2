                    <h3>Manutenção&nbsp;Eletromecânica</h3>

                    <p><br>
                        O técnico superior de Manutenção&nbsp;Eletromecânica<strong>&nbsp;</strong>é um profissional com capacidade para liderar equipas de manutenção e capaz de elaborar e implementar planos de manutenção de equipamentos e de instalações, fazendo a interligação com os técnicos de outras áreas como produção, qualidade, ambiente e segurança. Esta formação confere competências para técnicos de instalações e manutenção equivalente a TIM II e TIM III.</p>

                    <p>Constitui o ponto de partida para uma carreira técnica que poderá conhecer a sua continuidade no ensino superior ou no mercado de trabalho em múltiplos sectores de atividade.</p>

                    <p>Competências a adquirir:</p>

                    <ul>
