                        <li>Liderar e operacionalizar equipas de manutenção</li>
                        <li>Elaborar programas de concursos e cadernos de encargos;</li>
                        <li>Organizar e supervisionar o trabalho de equipas de manutenção, otimizando a produtividade;</li>
                        <li>Colaborar na elaboração e implementação de Sistemas de Gestão da Manutenção;</li>
                        <li>Colaborar no desenvolvimento de estudos de avaliação económica de equipamentos e infraestruturas.</li>
                    </ul>

