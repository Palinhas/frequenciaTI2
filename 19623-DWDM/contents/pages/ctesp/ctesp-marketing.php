                        <h3>Gestão de Vendas e Marketing<br>
                            &nbsp;</h3>

                        <p><strong>Objetivos</strong></p>

                        <p align="justify">O Curso de Técnico Superior Profissional de Gestão de Vendas e Marketing, tem como objetivo formar profissionais que colaborem em departamentos comerciais de empresas, com capacidade para liderar e gerir equipas de vendas e com competências para planear, coordenar e executar processos de negócios.</p>

                        <hr>
                        <p><strong>Competências</strong></p>

                        <p>Com esta oferta formativa pretende-se que o aluno adquira competências na area comercial:</p>

                        <ul>
                            <li>Aplicar técnicas de venda e de negociação;</li>
                            <li>Planificar e gerir atividades de marketing estratégico e operacional;</li>
                            <li>Planificar, gerir e executar políticas de marketing e vendas a nível nacional e internacional;</li>
                            <li>Elaborar planos de negócios que permitam o posicionamento nacional e internacional da organização;</li>
                            <li>Dinamizar a promoção da venda e o acompanhamento do cliente;</li>
                            <li>Definir e implementar soluções de comunicação da organização adequadas ao mercado;</li>
                            <li>Gerir a informação integrando de forma eficaz as diferentes aplicações informáticas de suporte à atividade;</li>
                            <li>Planear estratégias de colocação de produtos no mercado;</li>
                            <li>Gerir equipas de venda;</li>
                            <li>Gerir pontos&nbsp;de venda;</li>
                            <li>Monitorizar a aplicação, por parte da equipa de vendas, de técnicas de atendimento, venda e negociação.</li>
                        </ul>

                        <p><strong>Áreas relevantes</strong></p>

                        <ul>
                            <li>Economia ou&nbsp;Matemática ou Português.</li>
                        </ul>

