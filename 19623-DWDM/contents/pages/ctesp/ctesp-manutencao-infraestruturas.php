                        <h3>Manutenção de Equipamentos e Infraestruturas</h3>

                        <p><br>
                            <strong>Objetivos</strong></p>

                        <p align="justify">O Curso de Técnico Superior Profissional de Manutenção de Equipamentos e Infraestruturas, tem como objetivo formar técnicos habilitados a integrar e liderar equipas de manutenção de grandes edifícios de serviços e de habitação, tais como Hotéis, Hospitais, Centros de Saúde, Escolas, Câmaras Municipais, Organismos Estatais, Edifícios de Habitação, Condomínios de Moradias, etc. Estes técnicos serão capazes de elaborar e implementar planos de manutenção de edifícios (PIME) e intervir no aumento da eficiência das instalações.</p>

                        <hr>
                        <p><strong>Competências</strong></p>

                        <p>Com esta oferta formativa pretende-se que o aluno adquira competências na area da Manutenção de Edifícios:</p>

                        <ul>
                            <li>Definir e implementar planos e processos de manutenção de equipamentos;</li>
                            <li>Planear, coordenar e fiscalizar operações de manutenção de equipamentos e infraestruturas;</li>
                            <li>Executar e dirigir obras;</li>
                            <li>Elaborar planos de execução de obras;</li>
                            <li>Elaborar programas de concurso e cadernos de encargos;</li>
                            <li>Organizar e supervisionar o trabalho das equipas de manutenção, otimizando a produtividade;</li>
                            <li>Colaborar na elaboração e implementação de Sistemas de Gestão da Qualidade e da Energia;</li>
                            <li>Implementar planos de Segurança e Saúde.</li>
                        </ul>

