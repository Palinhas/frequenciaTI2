                    <h3>Secretariado de Administração</h3>

                    <p><br>
                        <strong>Objetivos</strong></p>

                    <p align="justify">O Curso de Técnico Superior Profissional em Secretariado de Administração, visa formar profissionais capazes de desempenhar funções de assistentes de direção e de gestão, organizando e planificando tarefas de suporte à administração e à gestão, utilizando ferramentas informáticas e de gestão documental, em ambiente empresarial.</p>

                    <hr>
                    <p><strong>Competências</strong></p>

                    <p>Com esta oferta formativa pretende-se que o aluno adquira competências na área do secretariado e trabalho administrativo</p>

                    <ul>
                        <li>Planificar e executar tarefas comunicacionais e relacionais com interlocutores, internos e externos, no âmbito das funções do assistente de administração;</li>
                        <li>Assegurar a comunicação da chefia/direção com interlocutores, internos e externos, em língua estrangeira (inglês e espanhol);</li>
                        <li>Planificar e executar tarefas inerentes à gestão e organização do secretariado;</li>
                        <li>Planificar e executar tarefas administrativas utilizando ferramentas TIC, quer para processamento de texto, cálculo, etc. quer no âmbito da gestão documental;</li>
                        <li>Planificar e executar tarefas de organização e preparação de documentação no apoio administrativo ao setor financeiro e comercial;</li>
                    </ul>

