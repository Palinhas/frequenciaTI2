                    <h3 align="justify">Reabilitação Energética e Conservação<br>
                        de Edifícios</h3>

                    <p align="justify"><br>
                        <strong>Objetivos</strong></p>

                    <p align="justify">O Curso de Técnico Superior Profissional de Reabilitação Energética e Conservação de Edifícios, intervém ao nível da reabilitação energética, da conservação e da manutenção do património edificado, integrado em equipas multidisciplinares, contribuindo para a melhoria da qualidade das construções, através do conhecimento das especificidades dos edifícios existentes e das implicações que as intervenções podem produzir ao nível da melhoria do seu comportamento, nomeadamente ao nível da avaliação do seu estado de conservação atual e identificação e definições de soluções técnicas de reabilitação e ou de conservação.</p>

                    <hr>
                    <p><strong>Competências</strong></p>

                    <ul>
                        <li>Definir e implementar soluções de reabilitação energética.</li>
                        <li>Planear, coordenar e fiscalizar operações de conservação, reabilitação e manutenção.</li>
                        <li>Analisar projetos - peças desenhadas e especificações relativas às características técnicas da construção ou da instalação.</li>
                        <li>Analisar os planos de execução da obra e otimizar a relação prazo, qualidade e custos.</li>
                        <li>Analisar o programa de concurso e cadernos de encargos e determinar as suas implicações no orçamento de uma obra.</li>
                        <li>Assistir tecnicamente a execução dos trabalhos das várias equipas e especialidades, tendo em conta os requisitos técnicos e de qualidade definidos.</li>
                        <li>Organizar, distribuir e supervisionar o trabalho das equipas de produção, otimizando a produtividade.</li>
                        <li>Participar em equipas multidisciplinares de organização e implementação de Planos da Qualidade e Sistemas de Gestão da Qualidade.</li>
                        <li>Participar em equipas multidisciplinares de organização e implementação de planos de manutenção.</li>
                        <li>Participar em equipas multidisciplinares de organização e implementação de planos de Segurança, Higiene e Saúde nos Estaleiros e Obras.</li>
                        <li>Saber avaliar o seu desempenho e o desempenho de terceiros.</li>
                    </ul>

