                    <h3>Desenvolvimento para a Web e Dispositivos Móveis</h3>
                    <p><br>
                        <strong>Objetivos</strong></p>
                    <p>O Curso de Técnico Superior Profissional de Desenvolvimento para a Web e Dispositivos Móveis<em><strong> </strong></em>tem como objetivo analisar, conceber, planear e desenvolver aplicações para as várias plataformas de dispositivos móveis e para a Internet.</p>
                    <hr>
                    <p><strong>Competências</strong></p>

                    <ul>
                        <li>Implementar soluções informáticas para dispositivos móveis nas plataformas mais comuns;</li>
                        <li>Implementar sistemas de informação baseados em tecnologias web através da utilização de sistemas de computação clássicos e de dispositivos móveis;</li>
                        <li>Desenhar e implementar bases de dados tendo em vista a resolução de problemas de negócio ou outros e de suporte aos respetivos sistemas de informação;</li>
                        <li>Planear e implementar a integração de sistemas informáticos;</li>
                        <li>Testar, instalar, configurar, migrar e distribuir as aplicações informáticas para ambientes web ou dispositivos móveis;</li>
                        <li>Organizar, distribuir e supervisionar o trabalho de equipas de desenvolvimento, otimizando a produtividade;</li>
                        <li>Gerir o ciclo de vida das aplicações, na sua manutenção e na sua atualização tecnológica continua face aos requisitos identificados;</li>
                        <li>Participar na análise de aplicações informáticas para ambientes web e dispositivos móveis;</li>
                        <li>Planear, distribuir e publicitar aplicações informáticas para ambientes web e dispositivos móveis.</li>
                    </ul>

