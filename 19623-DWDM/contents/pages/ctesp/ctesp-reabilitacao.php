                    <h3>Reabilitação de Construções Tradicionais</h3>

                    <p><br>
                        <strong>Objectivos</strong></p>

                    <p>O técnico superior de Reabilitação de Construções Tradicionais é um profissional habilitado a intervir na reabilitação da construção tradicional, através da implementação de planos e processos de reabilitação, conservação e restauro do património edificado, integrado em equipas multidisciplinares, contributo para a manutenção da integridade das construções, através do conhecimento das suas especificidades e das implicações que as intervenções podem produzir.</p>

                    <p>Estes técnicos terão competências para planear, dirigir e supervisionar obras de reabilitação e conservação, elaborar planos de execução, programas de concurso e cadernos de encargos.</p>

                    <p><strong>Competências</strong></p>

                    <ul>
                        <li>Supervisionar a utilização adequada das tecnologias e dos equipamentos na reabilitação de edifícios</li>
                        <li>Definir e implementar soluções de reabilitação e conservação</li>
                        <li>Planear, coordenar e fiscalizar operações de reabilitação e conservação.</li>
                        <li>Analisar projetos, peças desenhadas e especificações relativas às características técnicas da construção.</li>
                        <li>Dirigir obras e elaborar programas de concurso e cadernos de encargos.</li>
                        <li>Assistir tecnicamente a execução dos trabalhos das várias equipas e especialidades, tendo em conta os requisitos técnicos e de qualidade definidos.</li>
                        <li>Organizar, distribuir e supervisionar o trabalho das equipas de produção/manutenção, otimizando a produtividade.</li>
                        <li>Participar em equipas multidisciplinares de organização e implementação de planos de manutenção de construções .</li>
                        <li>Participar em equipas multidisciplinares de organização e implementação de planos de Segurança, Higiene e Saúde nos Estaleiros e Obras.&nbsp;</li>
                    </ul>

                    <p>&nbsp;</p>

