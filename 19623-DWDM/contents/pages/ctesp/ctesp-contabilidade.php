                    <h3 align="justify">Contabilidade</h3>
                    <p align="justify"><br>
                        <strong>Objetivos</strong></p>
                    <p align="justify">O Curso de Técnico Superior Profissional de Contabilidade, tem como objectivo contribuir para a gestão dos sistemas de informação financeira da organização, pública ou privada, ao nível do registo contabilístico e do planeamento e controlo dos processos internos de gestão, de forma a produzir informação relevante de apoio à administração e direção na tomada de decisões.</p>
                    <hr>
                    <p><strong>Competências</strong></p>

                    <p>Com esta oferta formativa pretende-se que o aluno adquira competências na area de contabilidade e fiscalidade:</p>

                    <ul>
                        <li>Conhecimentos fundamentais das formas de organização e tratamento da informação de suporte à contabilidade;</li>
                        <li>Conhecimentos fundamentais de contabilidade financeira, de gestão e contabilidades setoriais;</li>
                        <li>Conhecimentos especializados de enquadramento económico e legal das empresas (fiscalidade, societário e do trabalho);</li>
                        <li>Conhecimentos especializados de contabilidade de gestão;</li>
                        <li>Conhecimentos especializados que permitam a produção de relatórios e informações para a tomada de decisão;</li>
                        <li>Conhecimentos fundamentais de gestão;</li>
                        <li>Conhecimentos fundamentais de auditoria e controlo interno;</li>
                        <li>Conhecimentos fundamentais de língua inglesa;</li>
                        <li>Conhecimentos especializados de informática ao nível da preparação da informação financeira;</li>
                        <li>Conhecimentos especializados do processamento salarial e respectivo relato.</li>
                    </ul>

