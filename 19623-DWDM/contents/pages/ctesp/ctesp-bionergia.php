                        <p><strong>Objetivos</strong></p>

                        <p>O Curso de Técnico Superior Profissional de Bioenergias, tem como objetivo de formar quadros capazes de montar, operar e gerir sistemas de produção de biocombustíveis sólidos, líquidos e gasosos.</p>

