                        <p>Com esta oferta formativa pretende-se que o aluno adquira competências na área da Bioenergias:</p>

                        <ul>
                            <li>Desenvolver e gerir sistemas de produção de energia com base em biomassa;</li>
                            <li>Elaborar e gerir sistemas de produção de biocombustíveis;</li>
                            <li>Coordenar os trabalhos das várias equipas e especialidades, tendo em conta os requisitos técnicos e de qualidade definidos para as unidades de produção energia com base em biomassa e de biocombustíveis;</li>
                            <li>Organizar, distribuir e supervisionar o trabalho das equipas de produção, otimizando a produtividade nas unidades de produção energia com base em biomassa e de biocombustíveis;</li>
                            <li>Desenvolver planos de manutenção, qualidade e segurança nas unidades de produção de energia com base em biomassa e de biocombustíveis.</li>
                        </ul>

